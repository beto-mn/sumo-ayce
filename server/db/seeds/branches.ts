export async function seedBranches() {
  console.log(`  Seeding ${BRANCHES.length} branches…`)

  for (const branch of BRANCHES) {
    await db
      .insert(branches)
      .values({
        code: branch.code,
        name: branch.name,
        state: branch.state,
        address: branch.address,
        type: branch.type,
        lat: branch.lat,
        lng: branch.lng,
        schedule: branch.schedule,
        isActive: true,
      })
      .onConflictDoUpdate({
        target: branches.code,
        set: {
          name: branch.name,
          state: branch.state,
          address: branch.address,
          type: branch.type,
          lat: branch.lat,
          lng: branch.lng,
          schedule: branch.schedule,
          whatsappReservaciones: branch.whatsappReservaciones ?? null,
          isActive: true,
        },
      })
  }

  console.log(`  ✓ ${BRANCHES.length} branches done`)
}
